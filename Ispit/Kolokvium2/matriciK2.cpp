//Да се напише програма која вчитува матрица со димензии MxN (макс. 100x100). На почетокот се внесуваат димензиите на матрицата,
//а потоа и елементите на матрицата кои се само вредностите 1 и 0. Програмата треба да изброи и отпечати на СИ во колку од
//редиците и колоните има барем 3 последователни елементи со вредност 1.

#include <iostream>
using namespace std;

int main() {
    int M, N;

    // Внеси ги димензиите на матрицата
    cin >> M >> N;

    // Внеси ги елементите на матрицата
    int matrica[100][100];
    for (int i = 0; i < M; i++) {
        for (int j = 0; j < N; j++) {
            cin >> matrica[i][j];
        }
    }

    // Изброј и отпечати колку редици имаат барем 3 последователни елементи со вредност 1
    int brojacRedici = 0;
    for (int i = 0; i < M; i++) {
        int brojac = 0;
        for (int j = 0; j < N; j++) {
            if (matrica[i][j] == 1) {
                brojac++;
                if (brojac >= 3) {
                    brojacRedici++;
                    break; // Нема потреба да продолжуваме проверка за оваа редица
                }
            } else {
                brojac = 0; // Ресетирај бројач ако се појави 0
            }
        }
    }

    // Изброј и отпечати колку колони имаат барем 3 последователни елементи со вредност 1
    int brojacKoloni = 0;
    for (int j = 0; j < N; j++) {
        int brojac = 0;
        for (int i = 0; i < M; i++) {
            if (matrica[i][j] == 1) {
                brojac++;
                if (brojac >= 3) {
                    brojacKoloni++;
                    break; // Нема потреба да продолжуваме проверка за оваа колона
                }
            } else {
                brojac = 0; // Ресетирај бројач ако се појави 0
            }
        }
    }

    // Отпечати резултатите
    cout << brojacRedici + brojacKoloni << endl;

    return 0;
}
